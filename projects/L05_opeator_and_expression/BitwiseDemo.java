package L05_opeator_and_expression;

public class BitwiseDemo {
    public static void main(String[] args) {
        int x = 10, y = 6;
        System.out.println(x & y);
        System.out.println(x | y);
        System.out.println(x ^ y);
        System.out.println(x << 1);
        System.out.println(x >> 1);
    }
}
